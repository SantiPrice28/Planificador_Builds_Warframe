    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        String nombreUsuario = txt_nombre_usuario.getText().trim();
        char[] pass1 = txt_contrasena.getPassword();
        char[] pass2 = txt_contrasena_repetir.getPassword();

        String contrasena1 = new String(pass1);
        String contrasena2 = new String(pass2);

        // Verificar que las contrase単as coinciden
        if (!contrasena1.equals(contrasena2)) {
            JOptionPane.showMessageDialog(this, "Las contrase単as no coinciden");
            return;
        }

        ControladorPrincipal.registrarUsuario(nombreUsuario, contrasena1);

        // Limpiar campos
        txt_nombre_usuario.setText("");
        txt_contrasena.setText("");
        txt_contrasena_repetir.setText("");

        // Cerrar ventana
        this.dispose();
    }//GEN-LAST:event_btn_registrarActionPerformed

